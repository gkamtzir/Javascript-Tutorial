//Switch
var age = 5;

switch (age) {

case 5:

    console.log('You are 5 years old');
    break;

case 10:

    console.log('You are 10 years old');
    break;

}

//Default
age = 15;

switch (age) {

case 5:

    console.log('You are 5 years old');
    break;

case 10:

    console.log('You are 10 years old');
    break;

default:

    console.log('Not 5, neither 10');

}
